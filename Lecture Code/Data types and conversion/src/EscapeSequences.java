public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello\bOnePiece...");
        System.out.println("Hello\tOnePiece...");
        System.out.println("Hello\nOnePiece...");
        System.out.println("Hello \"OnePiece\"...");
        System.out.println("Hello \\OnePiece\\...");
        System.out.println("Hello \'OnePiece\'...");
    }
}
